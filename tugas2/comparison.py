#oprator >
print('apakah benar 5>3 ?' ,5>3)
print('apakah benar 5>5 ?' ,5>5)

#oprator <
print('apakah benar 5<3 ?' ,5<3)
print('apakah benar 5<5 ?' ,5<5)

#oprator >=
print('apakah benar 5>=3 ?' ,5>=3)
print('apakah benar 5>=5 ?' ,5>=5)

#oprator <=
print('apakah benar 5<=3 ?' ,5<=3)
print('apakah benar 5<=5 ?' ,5<=5)

#oprator ==
print('apakah benar 5==3 ?' ,5==3)
print('apakah benar 5==5 ?' ,5==5)

#oprator !=
print('apakah benar 5!=3 ?' ,5!=3)
print('apakah benar 5!=5 ?' ,5!=5)